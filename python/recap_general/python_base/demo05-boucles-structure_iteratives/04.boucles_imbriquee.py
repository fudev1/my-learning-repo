# ------------------------------------------------------------------------------
# Boucle imbriquée
# ------------------------------------------------------------------------------

for i in range(3):          
    for j in range(4):      
        print(f'{i}, {j}')  

# Affiche:
# 0, 0
# 0, 1
# 0, 2
# 1, 0
# 1, 1
# 1, 2
# 2, 0
# 2, 1
# 2, 2

# une boucle imbriquée est un bloc de code qui contient d'autres blocs de code
# il est possible de répéter des instructions dans une boucle imbriquée
