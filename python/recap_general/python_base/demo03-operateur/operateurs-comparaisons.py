## Comparaisons 

a = 5
b = 3

# Egalité: ==
print("a == b", a == b) # False

# Différence: !=
print("a != b", a != b) # True

# Supérieur: >
print("a > b", a > b) # False

# Inférieur: <
print("a < b", a < b) # True

# Supérieur ou égal: >=
print("a >= b", a >= b) # False

# Inférieur ou égal: <=
print("a <= b", a <= b) # True
